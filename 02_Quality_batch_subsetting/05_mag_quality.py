derep_df.columns = derep_df.columns.str.strip()

# Extract Bin_ID more flexibly: bin.4, bin_4 etc.
checkm2_df["Bin_ID"] = checkm2_df["Name"].astype(str).str.extract(r"(bin[._]?\d+)")

# Extract Sample_ID flexibly: SRR, ERR, DRR IDs
checkm2_df["Sample_ID_extracted"] = checkm2_df["Name"].astype(str).str.extract(r"((?:SRR|ERR|DRR)\d+)")

# Clean dereplicated bins (remove .fa if present)
derep_df["Bins"] = derep_df["Bins"].astype(str).str.replace(".fa$", "", regex=True)

# Merge on Sample_ID + Bin
merged = pd.merge(
    derep_df,
    checkm2_df[
        [
            "Sample_ID_extracted", "Bin_ID",
            "Completeness", "Contamination",
            "Completeness_Model_Used", "Translation_Table_Used",
            "Coding_Density", "Contig_N50", "Average_Gene_Length",
            "Genome_Size", "GC_Content", "Total_Coding_Sequences",
            "Total_Contigs", "Max_Contig_Length"
        ]
    ],
    left_on=["Sample_ID", "Bins"],
    right_on=["Sample_ID_extracted", "Bin_ID"],
    how="left"
).drop(columns=["Sample_ID_extracted", "Bin_ID"])

# Save new merged file
