# all MAGS (Non-derep) with thick plot borders

import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from scipy.stats import pearsonr, spearmanr

# ============================
# Load and Combine Datasets
# ============================

files = {
    "Public Transport": "checkm2_public_transport_final",
    "Hospital Environment": "checkm2_hosp_env",
    "Hospital Sewage": "checkm2_hosp_sewage_final",
    "Ambulance": "checkm2_ambulance_final"
}

dfs = []
for source, path in files.items():
    df = pd.read_csv(path, sep="\\t", engine='python')
    df["Sample_Source"] = source
    dfs.append(df)

combined_df = pd.concat(dfs, ignore_index=True)

# ✅ Standardize Sample_Source labels
label_map = {
    "Ambulance": "Ambulance",
    "Hospital Environment": "Hosp_env",
    "Hospital Sewage": "Hosp_sewage",
    "Public Transport": "Public_transp"
}
combined_df["Sample_Source"] = combined_df["Sample_Source"].replace(label_map)

# Save combined data
