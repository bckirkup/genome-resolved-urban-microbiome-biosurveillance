# Canonical order & palette
source_order = ["Ambulance", "Hosp_env", "Hosp_sewage", "Public_transp"]
palette = {
    "Public_transp": "#1f77b4",
    "Hosp_env": "#aec7e8",
    "Hosp_sewage": "#ff7f0e",
    "Ambulance": "#fdbf6f"
}

# ============================
# Classify MAGs by Quality
# ============================
def classify_quality(row):
    if row.get('Completeness', 0) >= 90 and row.get('Contamination', 100) <= 5:
        return 'High-quality'
    elif row.get('Completeness', 0) >= 50 and row.get('Contamination', 100) <= 10:
        return 'Medium-quality'
    else:
        return 'Low-quality'

combined_df["Quality_Tier"] = combined_df.apply(classify_quality, axis=1)

# ============================
