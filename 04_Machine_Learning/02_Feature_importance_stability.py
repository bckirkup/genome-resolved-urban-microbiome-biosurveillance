# ===============================
#  Feature importance & Stability runs
# ===============================
import numpy as np, pandas as pd, time, os
from sklearn.model_selection import StratifiedKFold
from sklearn.ensemble import RandomForestClassifier
from sklearn.tree import DecisionTreeClassifier
from sklearn.inspection import permutation_importance

# ---------- Load & align ----------
tpm = pd.read_csv("TPM_clr_batch_corrected_v1.csv", index_col=0)  # species x samples
meta = pd.read_csv("Merged_metadata_all.csv")
common = tpm.columns.intersection(meta["Sample_ID"])
tpm = tpm[common]
meta = meta[meta["Sample_ID"].isin(common)].reset_index(drop=True)
tpm = tpm[meta["Sample_ID"]]

X = tpm.T.values
y = meta["Group"].values
species = tpm.index
print(f"Features: {X.shape} | Labels: {np.unique(y)} counts: {pd.Series(y).value_counts().to_dict()}")

# ---------- Settings ----------
n_repeats     = 50      # stability repeats
top_k         = 60      # only top-K features for permutation
perm_repeats  = 3       # repeats inside permutation importance
save_every    = 5       # partial save cadence (repeats)

# Use your tuned params (from Nested CV)
rf_params = {"n_estimators": 500, "max_depth": None, "max_features": 0.5, "min_samples_leaf": 1}
dt_params = {"max_depth": 5, "min_samples_leaf": 3, "min_samples_split": 2}

cv = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

rf_importances, dt_importances, perm_importances = [], [], []

# Ensure output dir exists
os.makedirs("importance_partials", exist_ok=True)

for repeat in range(1, n_repeats + 1):
    for fold, (train_idx, test_idx) in enumerate(cv.split(X, y), 1):
        t0 = time.time()
        X_tr, X_te = X[train_idx], X[test_idx]
        y_tr, y_te = y[train_idx], y[test_idx]

        # ---- 1) Fit RF on ALL features; record its Gini importances ----
        rf_full = RandomForestClassifier(
            random_state=repeat, n_jobs=-1, class_weight="balanced", **rf_params
        )
        rf_full.fit(X_tr, y_tr)
        rf_importances.append(rf_full.feature_importances_)

        # ---- 2) Fit DT on ALL features; record its importances ----
        dt = DecisionTreeClassifier(
            random_state=repeat, class_weight="balanced", **dt_params
        )
        dt.fit(X_tr, y_tr)
        dt_importances.append(dt.feature_importances_)

        # ---- 3) Permutation importance on top-K features ONLY ----
        # pick top-K indices from the full-RF importances
        top_idx = np.argsort(rf_full.feature_importances_)[-top_k:]

        # REFIT a *new* RF on only those K columns
        X_tr_k = X_tr[:, top_idx]
        X_te_k = X_te[:, top_idx]

        rf_k = RandomForestClassifier(
            random_state=repeat, n_jobs=-1, class_weight="balanced", **rf_params
        )
        rf_k.fit(X_tr_k, y_tr)

        # Now permute those same K columns
        perm = permutation_importance(
            rf_k, X_te_k, y_te, n_repeats=perm_repeats, random_state=repeat, n_jobs=-1
        )

        # place the K importances back into a length-1104 vector
        perm_vec = np.zeros(X.shape[1])
        perm_vec[top_idx] = perm.importances_mean
        perm_importances.append(perm_vec)

        print(f"Repeat {repeat}/{n_repeats}, Fold {fold}/5 in {time.time()-t0:.1f}s")

    # ---- Partial save every `save_every` repeats ----
    if repeat % save_every == 0:
        pd.DataFrame(rf_importances, columns=species).to_csv(
            f"importance_partials/RF_importances_partial_{repeat}.csv", index=False
        )
        pd.DataFrame(dt_importances, columns=species).to_csv(
            f"importance_partials/DT_importances_partial_{repeat}.csv", index=False
        )
        pd.DataFrame(perm_importances, columns=species).to_csv(
            f"importance_partials/Permutation_importances_partial_{repeat}.csv", index=False
        )
        print(f"Partial save @ repeat {repeat}")
        # (Optional) also flush to Drive if mounted: from google.colab import drive; drive.flush_and_unmount()

# ---------- Final save ----------
pd.DataFrame(rf_importances, columns=species).to_csv("RF_importances_repeated.csv", index=False)
pd.DataFrame(dt_importances, columns=species).to_csv("DT_importances_repeated.csv", index=False)
pd.DataFrame(perm_importances, columns=species).to_csv("Permutation_importances_topK.csv", index=False)

print("Finished! All importances saved.")

