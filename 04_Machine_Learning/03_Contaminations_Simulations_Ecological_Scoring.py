# Contamination Risk Scoring + Sensitivity Analysis
# ===============================

import pandas as pd, numpy as np, matplotlib.pyplot as plt, seaborn as sns
from sklearn.decomposition import PCA
from scipy.stats import spearmanr

# -------------------------
# Step 1. Load core inputs
# -------------------------
switch_df = pd.read_csv("synthetic_transition_points.csv")
blend_df  = pd.read_csv("synthetic_blend_entropy.csv")
pivot_df  = pd.read_csv("TPM_clr_batch_corrected_v1.csv", index_col=0).T
pivot_df["Group"] = pd.read_csv("Merged_metadata_all.csv").set_index("Sample_ID").loc[pivot_df.index, "Group"]
species_cols = pivot_df.columns.drop("Group")

# -------------------------
# Step 2. Compute risk metrics
# -------------------------
# (i) Overridden counts
loser_counts = switch_df["Switch_From"].value_counts()
loser_counts.name = "Times_Overridden"

# (ii) Entropy
env_entropy = {}
for env in blend_df["Env_A"].unique():
    mask = (blend_df["Env_A"] == env) | (blend_df["Env_B"] == env)
    env_entropy[env] = blend_df[mask].groupby("Blend_A")["Entropy"].mean().mean()
entropy_df = pd.DataFrame.from_dict(env_entropy, orient="index", columns=["Mean_Entropy"])

# (iii) Richness + variance
richness_dict, variance_dict = {}, {}
for env in pivot_df["Group"].unique():
    samples = pivot_df[pivot_df["Group"] == env][species_cols]
    richness_dict[env] = (samples > 0).sum(axis=1).mean()
    variance_dict[env] = samples.var(axis=1).mean()

richness_df = pd.DataFrame.from_dict(richness_dict, orient="index", columns=["Richness"])
variance_df = pd.DataFrame.from_dict(variance_dict, orient="index", columns=["Variance"])

# Merge all
risk_df = pd.concat([loser_counts, entropy_df, richness_df, variance_df], axis=1).fillna(0)

# Normalize
for col in ["Times_Overridden", "Mean_Entropy", "Richness", "Variance"]:
    risk_df[f"Norm_{col}"] = (risk_df[col] - risk_df[col].min()) / (risk_df[col].max() - risk_df[col].min())

# -------------------------
# Step 3. Three scoring schemes
# -------------------------
# A) Primary weights
risk_df["Risk_Primary"] = (
    0.4 * risk_df["Norm_Times_Overridden"] +
    0.3 * risk_df["Norm_Mean_Entropy"] +
    0.15 * risk_df["Norm_Richness"] +
    0.15 * risk_df["Norm_Variance"]
)

# B) Equal weights
risk_df["Risk_Equal"] = risk_df[
    ["Norm_Times_Overridden","Norm_Mean_Entropy","Norm_Richness","Norm_Variance"]
].mean(axis=1)

# C) PCA weights (PC1 loadings scaled positive)
X = risk_df[["Norm_Times_Overridden","Norm_Mean_Entropy","Norm_Richness","Norm_Variance"]].values
pca = PCA(n_components=1).fit(X)
weights = np.abs(pca.components_[0]) / np.sum(np.abs(pca.components_[0]))
risk_df["Risk_PCA"] = (X @ weights)

# -------------------------
# Step 4. Rank comparison
# -------------------------
rankings = risk_df[["Risk_Primary","Risk_Equal","Risk_PCA"]].rank(ascending=False)
correlations = pd.DataFrame(index=rankings.columns, columns=rankings.columns)

for i in rankings.columns:
    for j in rankings.columns:
        rho, _ = spearmanr(rankings[i], rankings[j])
        correlations.loc[i,j] = round(rho,3)

# Save
risk_df.to_csv("environment_risk_scores_all.csv")
correlations.to_csv("risk_score_rank_correlations.csv")

print("Risk scores computed + saved.")
print("\nRank correlations between schemes:\n", correlations)

