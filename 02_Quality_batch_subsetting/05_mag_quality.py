def has_cols(df, cols):
    return all(c in df.columns for c in cols)

def style_axes(ax, spine_lw=2.8, tick_w=2.5):
    """Thick black axes and ticks, no grid."""
    ax.grid(False)
    ax.minorticks_on()
    for s in ax.spines.values():
        s.set_edgecolor("black")
        s.set_linewidth(spine_lw)
    ax.tick_params(which="both", width=tick_w)
    ax.tick_params(which="major", length=6)
    ax.tick_params(which="minor", length=3)

# ============================
# KDE Plots
# ============================
def plot_kde(metric, filename, xlabel):
    if metric not in combined_df.columns:
        print(f"[skip] {metric} not found.")
        return
    sns.set_style("whitegrid")
    fig, ax = plt.subplots(figsize=(5, 5))
    sns.kdeplot(
        data=combined_df,
        x=metric,
        hue="Sample_Source",
        hue_order=source_order,
        fill=True, alpha=0.5,
        linewidth=1.6,
        ax=ax, palette=palette
    )
    ax.set_xlabel(xlabel, fontsize=10, color='black')
    ax.set_ylabel("Density", fontsize=10, color='black')
    style_axes(ax)

    # keep legend but remove title
    leg = ax.get_legend()
    if leg is not None:
        leg.set_title("")

    plt.savefig(filename, dpi=600, bbox_inches="tight")
    plt.close()

plot_kde("Completeness", "completeness_distribution.png", "Completeness (%)")
