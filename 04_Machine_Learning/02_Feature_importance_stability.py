import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

# ---- Load saved results ----
rf_df   = pd.read_csv("RF_importances_repeated.csv")     # repeats × species
dt_df   = pd.read_csv("DT_importances_repeated.csv")
perm_df = pd.read_csv("Permutation_importances_topK.csv")

species = rf_df.columns

# ---- Load metadata (for labels) ----
meta = pd.read_csv("Merged_metadata_all.csv")
y = meta["Group"].values

# ---- Load TPM for alignment ----
tpm = pd.read_csv("TPM_clr_batch_corrected_v1.csv", index_col=0)
common_samples = tpm.columns.intersection(meta["Sample_ID"])
tpm = tpm[common_samples]
meta = meta[meta["Sample_ID"].isin(common_samples)].reset_index(drop=True)
tpm = tpm[meta["Sample_ID"]]   # enforce same order

X = tpm.T.values

# ---- 1) Train RF per environment (one-vs-rest) ----
from sklearn.ensemble import RandomForestClassifier
from sklearn.model_selection import StratifiedKFold

rf_params = {"n_estimators": 500, "max_depth": None, "max_features": 0.5, "min_samples_leaf": 1}
cv = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

env_importances = {env: [] for env in np.unique(y)}

for env in np.unique(y):
    # binary labels: env vs rest
    y_bin = (y == env).astype(int)

    for tr_idx, te_idx in cv.split(X, y_bin):
        X_tr, X_te = X[tr_idx], X[te_idx]
        y_tr, y_te = y_bin[tr_idx], y_bin[te_idx]

        rf = RandomForestClassifier(
            random_state=42, n_jobs=-1, class_weight="balanced", **rf_params
        )
        rf.fit(X_tr, y_tr)
        env_importances[env].append(rf.feature_importances_)

# ---- 2) Aggregate mean importances per environment ----
env_mean = {
    env: np.mean(importances, axis=0) for env, importances in env_importances.items()
}
env_df = pd.DataFrame(env_mean, index=species)

# ---- 3) For each environment, rank top features ----
topN = 20
for env in env_df.columns:
    top_features = env_df[env].sort_values(ascending=False).head(topN)
    top_features.to_csv(f"TopFeatures_{env}.csv")

    plt.figure(figsize=(6,6))
    sns.barplot(
        x=top_features.values, y=top_features.index,
        color="steelblue"
    )
    plt.xlabel("Mean RF Importance")
    plt.ylabel("")
    plt.title("")
    plt.tight_layout()
    plt.savefig(f"TopFeatures_{env}_Barplot.tiff", dpi=600)
    plt.close()

print("Per-environment top features saved")

