def plot_stacked_quality_bar():
    counts = (
        combined_df.groupby(["Sample_Source", "Quality_Tier"])
        .size()
        .unstack(fill_value=0)
    )
    tier_order = ["High-quality", "Medium-quality", "Low-quality"]
    for t in tier_order:
        if t not in counts.columns:
            counts[t] = 0
    counts = counts[tier_order]
    counts = counts.reindex(source_order).fillna(0).astype(int)

    ax = counts.plot(
        kind="bar", stacked=True, figsize=(5, 5),
        edgecolor="black", width=0.7,
        color=['#1b9e77', '#d95f02', '#7570b3']
    )
    ax.set_xlabel("")
    ax.set_ylabel("Number of MAGs")
    plt.xticks(rotation=45)
    ax.grid(False)

    legend_labels = ["High-quality", "Medium-quality", "Low-quality"]
    handles, _ = ax.get_legend_handles_labels()
    leg = ax.legend(handles, legend_labels, title="", fontsize=8, title_fontsize=9, loc="best", frameon=True)
    if leg is not None:
        leg.set_title("")

    style_axes(ax)

    for i, (src, row) in enumerate(counts.iterrows()):
        total = int(row.sum())
        ax.text(i, total + max(1, total*0.01), str(total), ha='center', va='bottom', fontsize=8, weight='bold')

    plt.tight_layout()
    plt.savefig("quality_tier_stacked_bar.png", dpi=600)
    plt.close()

