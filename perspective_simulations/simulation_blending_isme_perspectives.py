# SYNTHETIC BLENDING AND ECOLOGICAL DOMINANCE (COUNTRY-LEVEL)
# ===========================================================

import pandas as pd, numpy as np, itertools
from sklearn.ensemble import RandomForestClassifier
from scipy.stats import entropy
import matplotlib.pyplot as plt, seaborn as sns

# -------------------------
# 0) Load data
# -------------------------
tpm = pd.read_csv("TPM_clr_batch_corrected_v1.csv", index_col=0)
meta = pd.read_csv("Merged_metadata.csv")

# Align data
common = tpm.columns.intersection(meta["Sample_ID"])
tpm = tpm[common]
meta = meta[meta["Sample_ID"].isin(common)].reset_index(drop=True)
tpm = tpm[meta["Sample_ID"]]

# Features and labels
X = tpm.T.values
y = meta["Group"].values  # Original environment labels (RF model trained on this)

# -------------------------
# 1) Load or train Random Forest model (trained on environments)
# -------------------------
rf_params = {
    "n_estimators": 500,
    "max_depth": None,
    "max_features": 0.5,
    "min_samples_leaf": 1,
    "random_state": 42,
    "n_jobs": -1,
    "class_weight": "balanced"
}

rf = RandomForestClassifier(**rf_params)
rf.fit(X, y)
print(f"✔ RandomForest trained — hold-out accuracy approx: {rf.score(X, y):.3f}")

# -------------------------
# 2) Prepare data grouped by COUNTRY
# -------------------------
pivot_df = pd.DataFrame(X, columns=tpm.index)
pivot_df["Sample_ID"] = meta["Sample_ID"].values
pivot_df["Country"] = meta["Country"].values
pivot_df["Continent"] = meta["Continent"].values
pivot_df["Group"] = meta["Group"].values  # For reference

# Filter to representative countries (edit as needed)
selected_countries = [
    "USA", "China", "Singapore", "Finland",
    "Benin", "Burkina Faso", "South Africa"
]
pivot_df = pivot_df[pivot_df["Country"].isin(selected_countries)]

# -------------------------
# 3) Synthetic mixing simulations (Country–Country)
# -------------------------
blend_ratios = np.linspace(0, 1, 11)  # 0–100% blending
countries = pivot_df["Country"].unique()
species_cols = tpm.index.tolist()

results = []

for cA, cB in itertools.combinations(countries, 2):
    print(f"🌍 Simulating: {cA} ↔ {cB}")
    samples_A = pivot_df[pivot_df["Country"] == cA][species_cols].values
    samples_B = pivot_df[pivot_df["Country"] == cB][species_cols].values
    n_samples = min(len(samples_A), len(samples_B))

    for alpha in blend_ratios:
        for i in range(n_samples):
            blend = alpha * samples_A[i] + (1 - alpha) * samples_B[i]
            proba = rf.predict_proba([blend])[0]
            pred_label = rf.classes_[np.argmax(proba)]
            conf = np.max(proba)
            ent = entropy(proba, base=2)
            results.append({
                "Country_A": cA, "Country_B": cB,
                "Blend_A": alpha, "Blend_B": 1 - alpha,
                "Predicted": pred_label,
                "Confidence": conf,
                "Entropy": ent
            })

blend_df = pd.DataFrame(results)
blend_df.to_csv("synthetic_blend_country_entropy.csv", index=False)
print("✔ Saved: synthetic_blend_country_entropy.csv")

# -------------------------
# 4) Identify switch points (tipping thresholds)
# -------------------------
switch_points = []
for (cA, cB), subset in blend_df.groupby(["Country_A", "Country_B"]):
    subset = subset.sort_values("Blend_A")
    preds = subset["Predicted"].values
    blends = subset["Blend_A"].values
    for i in range(1, len(preds)):
        if preds[i] != preds[i - 1]:
            switch_points.append({
                "Country_A": cA, "Country_B": cB,
                "Switch_From": preds[i - 1], "Switch_To": preds[i],
                "Blend_Percent_A": blends[i],
                "Blend_Percent_B": 1 - blends[i]
            })
            break

switch_df = pd.DataFrame(switch_points)
switch_df.to_csv("synthetic_country_switch_points.csv", index=False)
print("✔ Saved: synthetic_country_switch_points.csv")

