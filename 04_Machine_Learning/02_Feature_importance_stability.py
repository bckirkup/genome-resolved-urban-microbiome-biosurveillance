dt_mean   = dt_df.mean().rename("DT")
perm_mean = perm_df.mean().rename("Permutation")

agg = pd.concat([rf_mean, dt_mean, perm_mean], axis=1)

# ---- 2) Normalize each method to [0,1] for comparability ----
agg_norm = agg.div(agg.max())

# ---- 3) Rank within each method (1 = most important) ----
ranks = agg.rank(ascending=False)
agg["RF_rank"]   = ranks["RF"]
agg["DT_rank"]   = ranks["DT"]
agg["Perm_rank"] = ranks["Permutation"]

