X = tpm.T.values                   # samples x species
y = meta["Group"].values           # environment labels
classes = meta["Group"].unique()
print("✔ Data:", X.shape, "classes:", dict(pd.Series(y).value_counts()))

# -------------------------
# 1) Train/hold-out split & final model
# -------------------------
X_tr, X_te, y_tr, y_te = train_test_split(
    X, y, test_size=0.2, stratify=y, random_state=42
)

# Tuned params from nested CV
rf_params = {"n_estimators": 500, "max_depth": None, "max_features": 0.5, "min_samples_leaf": 1}
dt_params = {"max_depth": 5, "min_samples_leaf": 3, "min_samples_split": 2}

rf = RandomForestClassifier(random_state=42, n_jobs=-1, class_weight="balanced", **rf_params)
rf.fit(X_tr, y_tr)

