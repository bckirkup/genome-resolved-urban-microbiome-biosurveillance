# -------------------------
# 2) Helpers
# -------------------------
from math import sqrt

def wilson_ci(k, n, z=1.96):
    if n == 0: return (0.0, 0.0)
    p = k / n
    denom = 1 + z**2/n
    center = (p + z**2/(2*n)) / denom
    half = z * sqrt(p*(1-p)/n + z**2/(4*n**2)) / denom
    return max(0, center - half), min(1, center + half)

def mix_samples(x_rec, x_don, alpha):
    return (1.0 - alpha) * x_rec + alpha * x_don

def mix_centroids(mu_rec, mu_don, alpha):
    return (1.0 - alpha) * mu_rec + alpha * mu_don

def refine_alphas(coarse_alphas, a_hit, step=0.01):
    if np.isnan(a_hit): return coarse_alphas
    lo = max(0.0, a_hit - 0.05)
    hi = min(1.0, a_hit + 0.05)
    fine = np.arange(lo, hi + 1e-9, step)
    return np.unique(np.concatenate([coarse_alphas, fine]))

# Precompute centroids
by_env = {g: X[meta["Group"]==g] for g in classes}
centroids = {g: by_env[g].mean(axis=0) for g in classes}
env_list = list(by_env.keys())

# -------------------------
# 3) Simulation grid
# -------------------------
alphas = np.linspace(0, 1.0, 21)   # coarse grid (0.05 step)
n_pairs = 50                       # replicates per alpha
prob_thresh = 0.8

rng = np.random.default_rng(123)

records = []

for rec in env_list:
    for don in env_list:
        if rec == don: continue
        X_rec, X_don = by_env[rec], by_env[don]
        mu_r, mu_d   = centroids[rec], centroids[don]

        # ---- coarse pass (sample mode only) ----
        coarse_rows = []
        for a in alphas:
            cnt = 0
            for _ in range(n_pairs):
                x_r = X_rec[rng.integers(0, X_rec.shape[0])]
                x_d = X_don[rng.integers(0, X_don.shape[0])]
                x_mix = mix_samples(x_r, x_d, a).reshape(1, -1)
                cnt += (model.predict(x_mix)[0] == don)
            p = cnt / n_pairs
            lo, hi = wilson_ci(cnt, n_pairs)
            coarse_rows.append({"Mode":"sample","Recipient":rec,"Donor":don,"alpha":a,
                                "P_donor":p,"CI_low":lo,"CI_high":hi})
        coarse_df = pd.DataFrame(coarse_rows)
        hit = coarse_df[coarse_df["P_donor"] >= prob_thresh]
        a_hit = hit["alpha"].iloc[0] if len(hit) else np.nan
        # refine if needed
        a_refined = refine_alphas(alphas, a_hit, step=0.01)

        # ---- rerun with refined grid ----
        for a in a_refined:
            cnt = 0
            for _ in range(n_pairs):
                x_r = X_rec[rng.integers(0, X_rec.shape[0])]
                x_d = X_don[rng.integers(0, X_don.shape[0])]
                x_mix = mix_samples(x_r, x_d, a).reshape(1, -1)
                cnt += (model.predict(x_mix)[0] == don)
            p = cnt / n_pairs
            lo, hi = wilson_ci(cnt, n_pairs)
            records.append({"Mode":"sample","Recipient":rec,"Donor":don,"alpha":a,
                            "P_donor":p,"CI_low":lo,"CI_high":hi})

        # ---- centroid mode ----
        for a in a_refined:
            x_mix = mix_centroids(mu_r, mu_d, a).reshape(1, -1)
            y_hat = model.predict(x_mix)[0]
            p = float(y_hat == don)
            records.append({"Mode":"centroid","Recipient":rec,"Donor":don,"alpha":a,
                            "P_donor":p,"CI_low":p,"CI_high":p})

# -------------------------
# 4) Save dose–response
# -------------------------
sim_df = pd.DataFrame(records)
sim_df.to_csv("Simulations_donor_recipient_withCI.csv", index=False)
print("✔ Saved: Simulations_donor_recipient_withCI.csv")

