import pandas as pd, numpy as np, itertools
import matplotlib.pyplot as plt, seaborn as sns
from scipy.stats import entropy
from sklearn.ensemble import RandomForestClassifier
from sklearn.preprocessing import LabelEncoder

# -------------------------
# Load data
# -------------------------
tpm = pd.read_csv("TPM_clr_batch_corrected_v1.csv", index_col=0)
meta = pd.read_csv("Merged_metadata_all.csv")

# Align sample IDs
common = tpm.columns.intersection(meta["Sample_ID"])
tpm = tpm[common]
meta = meta[meta["Sample_ID"].isin(common)].reset_index(drop=True)
tpm = tpm[meta["Sample_ID"]]

# Features + labels
X = tpm.T.values
y = meta["Group"].values
species_cols = tpm.index.tolist()

le = LabelEncoder()
y_enc = le.fit_transform(y)

# -------------------------
# Use tuned RF parameters
# -------------------------
rf_params = {
    "n_estimators": 500,
    "max_depth": None,
    "max_features": 0.5,
    "min_samples_leaf": 1,
    "random_state": 42,
    "n_jobs": -1,
    "class_weight": "balanced"
}

print("Using tuned RF params:", rf_params)

# Train RF with tuned params
rf = RandomForestClassifier(**rf_params)
rf.fit(X, y_enc)

# Build pivot table for blending
pivot_df = pd.DataFrame(X, columns=species_cols)
pivot_df["Sample_ID"] = meta["Sample_ID"]
pivot_df["Group"] = meta["Group"]

# -------------------------
# Synthetic mixing simulation
# -------------------------
env_names = pivot_df["Group"].unique()
grouped = pivot_df.groupby("Group")

blend_ratios = np.linspace(0, 1, 11)  # 0–100% in steps of 10%
results = []

for env_a, env_b in itertools.combinations(env_names, 2):
    print(f"Simulating: {env_a} ↔ {env_b}")
    samples_a = grouped.get_group(env_a)[species_cols].values
    samples_b = grouped.get_group(env_b)[species_cols].values
    n_samples = min(len(samples_a), len(samples_b))

    for ratio in blend_ratios:
        for i in range(n_samples):
            blend = ratio * samples_a[i] + (1-ratio) * samples_b[i]
            proba = rf.predict_proba([blend])[0]
            pred_class = np.argmax(proba)
            pred_label = le.inverse_transform([pred_class])[0]
            conf = np.max(proba)
            ent = entropy(proba, base=2)

            results.append({
                "Env_A": env_a, "Env_B": env_b,
                "Blend_A": ratio, "Blend_B": 1-ratio,
                "Predicted": pred_label,
                "Confidence": conf, "Entropy": ent
            })

# Save blend-level results
blend_df = pd.DataFrame(results)
blend_df.to_csv("synthetic_blend_entropy.csv", index=False)

# -------------------------
# Extract switch points
# -------------------------
switch_points = []
for (env_a, env_b), group in blend_df.groupby(["Env_A","Env_B"]):
    group = group.sort_values("Blend_A")
    preds = group["Predicted"].values
    blends = group["Blend_A"].values

    for i in range(1, len(preds)):
        if preds[i] != preds[i-1]:
            switch_points.append({
                "Env_A": env_a, "Env_B": env_b,
                "Switch_From": preds[i-1], "Switch_To": preds[i],
                "Blend_Percent_A": blends[i], "Blend_Percent_B": 1-blends[i]
            })
            break

switch_df = pd.DataFrame(switch_points)
switch_df.to_csv("synthetic_transition_points.csv", index=False)

print("Finished Part A: Generated synthetic_blend_entropy.csv and synthetic_transition_points.csv")

