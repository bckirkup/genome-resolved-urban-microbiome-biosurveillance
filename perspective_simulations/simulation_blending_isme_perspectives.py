import numpy as np, pandas as pd
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestClassifier
from math import sqrt
import matplotlib.pyplot as plt
import seaborn as sns

# -------------------------
# 0. Load data
# -------------------------
tpm = pd.read_csv("TPM_clr_batch_corrected_v1.csv", index_col=0)
meta = pd.read_csv("Merged_metadata.csv")

common = tpm.columns.intersection(meta["Sample_ID"])
tpm = tpm[common]
meta = meta[meta["Sample_ID"].isin(common)].reset_index(drop=True)
tpm = tpm[meta["Sample_ID"]]

X = tpm.T.values
y = meta["Country"].values
countries = meta["Country"].unique()
print(f"✔ Data loaded: {X.shape}, countries = {len(countries)}")

# -------------------------
# 1. Train model
# -------------------------
rf = RandomForestClassifier(
    n_estimators=500, max_depth=None, max_features=0.5,
    class_weight="balanced", random_state=42, n_jobs=-1
)
X_tr, X_te, y_tr, y_te = train_test_split(X, y, test_size=0.2, stratify=y, random_state=42)
rf.fit(X_tr, y_tr)
print(f"✔ RandomForest trained — hold-out accuracy: {rf.score(X_te, y_te):.3f}")

# -------------------------
# 2. Define helpers
# -------------------------
def wilson_ci(k, n, z=1.96):
    if n == 0: return (0.0, 0.0)
    p = k / n
    denom = 1 + z**2/n
    center = (p + z**2/(2*n)) / denom
    half = z * sqrt(p*(1-p)/n + z**2/(4*n**2)) / denom
    return max(0, center - half), min(1, center + half)

def mix_samples(x_rec, x_don, alpha):
    return (1-alpha) * x_rec + alpha * x_don

# -------------------------
# 3. Run synthetic blending simulations
# -------------------------
alphas = np.linspace(0, 1, 21)
n_pairs = 30
records = []

grouped = {c: X[meta["Country"] == c] for c in countries}
rng = np.random.default_rng(42)

for rec in countries:
    for don in countries:
        if rec == don: continue
        X_rec, X_don = grouped[rec], grouped[don]
        for a in alphas:
            cnt = 0
            for _ in range(n_pairs):
                x_r = X_rec[rng.integers(0, len(X_rec))]
                x_d = X_don[rng.integers(0, len(X_don))]
                x_mix = mix_samples(x_r, x_d, a).reshape(1, -1)
                cnt += (rf.predict(x_mix)[0] == don)
            p = cnt / n_pairs
            lo, hi = wilson_ci(cnt, n_pairs)
            records.append({"Recipient": rec, "Donor": don, "alpha": a, "P_donor": p, "CI_low": lo, "CI_high": hi})

sim_df = pd.DataFrame(records)
sim_df.to_csv("Country_DonorRecipient_Simulations.csv", index=False)
print("✔ Simulation completed and saved.")

# -------------------------
# 4. Minimal Detectable Contamination (MDC)
# -------------------------
def min_detectable_alpha(sub, thresh=0.8):
    sub = sub.sort_values("alpha")
    hit = sub[sub["P_donor"] >= thresh]
    return hit["alpha"].iloc[0] if len(hit) else np.nan

mdc_rows = []
for rec in countries:
    for don in countries:
        if rec == don: continue
        sub = sim_df[(sim_df["Recipient"]==rec)&(sim_df["Donor"]==don)]
        mdc_rows.append({"Recipient":rec,"Donor":don,"MDC":min_detectable_alpha(sub)})
mdc_df = pd.DataFrame(mdc_rows)
mdc_df.to_csv("Country_MDC_Table.csv", index=False)
print("✔ MDC table saved.")

